class Product {
  final String name;
  final int reviews;
  final int price;
  final String description;
  final double rating;
  final String image;
  final String category;

  Product({
    required this.name,
    required this.reviews,
    required this.price,
    required this.description,
    required this.rating,
    required this.image,
    required this.category,
  });
}
